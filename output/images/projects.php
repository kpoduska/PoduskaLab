<a name = "Environment"></a>  <h2>Carbon-rich solids: formation and degradation</h2>

<TABLE border="0" cellpadding="4" cellspacing="10" >
<TR align="left" > 

<TD width="60" height="60"  bgcolor="#999999" align="left" valign="center"  > <img src="cracks.jpg" title="cracks in ice"width="60">  </TD>

<TD width="*"  align="left" valign="center" >  Carbonate minerals are important sinks for atmospheric carbon dioxide, but their degradation can release the carbon dioxide back into the air or could contribute to pH changes in aqueous environments. This means that improving our understanding of processes such as ocean acidification, water remediation, and atmosphere-water interactions requires a better understanding of the complex interplay between carbonate minerals and their local environment.  <a href="?content=pubs&topic=research">We use laboratory experiments, as well as theoretical simulations, to investigate the evolution of and variations within carbonate mineral structures.</a>  <br><br> Collaborators include: <a href="https://chemistry.technion.ac.il/en/team/asher-schmidt/">Asher Schmidt</a> (Technion - Israel Institute of Technology), <a href="http://www.weizmann.ac.il/materials/Leeor/">Leeor Kronik</a> (Weizmann Institute of Science), <a href="https://www.physics.mun.ca/~saika/">Ivan Saika-Voivod</a> (Memorial Physics)
</TD>
</tr>
</TABLE>

