<TD width="*"  align="left" valign="center" > During earthquakes, strong waves can change the material through which they travel, often slowing the seismic wave. However, it is still an open question as to which material factors most influence seismic wave speeds? <a href="?content=pubs&topic=research">Our lab-scale experiments show clues by tracking wave speeds in porous rock.</a>  We are especially interested in exploring both the wave physics and the materials physics that are important for understanding dynamic pump-probe experiments.<br><br> Collaborators include: <a href="http://www.esd.mun.ca/~amalcolm/">Alison Malcolm</a> (Memorial Earth Sciences)
