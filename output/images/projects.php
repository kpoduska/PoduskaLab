<a name = "Biomaterials"></a> <h2>Bone-like biomaterials</h2>

<TABLE border="0" cellpadding="4" cellspacing="10" >
<TR align="left" > 

<TD width="60" height="60"  bgcolor="#999999" align="left" valign="center"  > <img src="collagen.jpg" title="collagen fibrils"width="60">  </TD>

<TD width="*"  align="left" valign="center" >  Coatings that mimic the composition of natural biomaterials
have a wide range of potential medical applications. For example, hydroxyapatite (Ca<sub>10</sub>(PO<sub>4</sub>)<sub>6</sub>(OH)<sub>2</sub>) is a compound that has been studied extensively because of its resemblance to the mineral phase in bone. Adding collagen or other proteins to mineral coatings to form a bioinorganic composite material can increase the biocompatibility of the coating. <a href="?content=pubs&topic=research">We use electrochemical methods to make mineral films and collagen membranes, and we study the mineral-protein symbiosis involved in making biocomposite coatings.</a> <br><br> Collaborators include: <a href="https://www.mun.ca/faculty/emerschrod/">Erika Merschrod</a> (Memorial Chemistry), <a href="http://www.med.mun.ca/Medicine/Faculty/Gendron,-Robert.aspx">Bob Gendron</a> and  <a href="http://www.med.mun.ca/Medicine/Faculty/Paradis,-Helene.aspx">Helene Paradis</a> (Memorial Medicine), <a href="https://islandscholar.ca/people/lmcduffee">Laurie McDuffee</a> (Atlantic Veterinary College and University of Prince Edward Island)</TD>
